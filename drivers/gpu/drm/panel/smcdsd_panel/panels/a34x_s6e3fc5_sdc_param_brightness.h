static inline void fill_gamma_offset(int unused)
{
	int ibr, gs, c, max;
	int *res, *mtp, *offset;
	u8 *gamma_reg_rx, *gamma_reg_tx;

	memset(gamma_set_rx, 0, sizeof(gamma_set_rx));
	memset(gamma_set_tx, 0, sizeof(gamma_set_tx));

	gamma_reg_rx = &RX_BUF_67[167];
	for (gs = 0; gs < GS_MAX; gs++) {
		for (c = 0; c < CI_MAX; c++) {
			gamma_set_rx[(gs * CI_MAX) + c] = get_unaligned_be16(gamma_reg_rx);
			gamma_reg_rx += DIV_ROUND_UP(gamma_width[gs], BITS_PER_BYTE);	/* 2reg per 1set */
		}
	}

	for (ibr = 0; ibr <= EXTEND_BRIGHTNESS; ibr++) {
		for (gs = 0; gs < GS_MAX; gs++) {
			mtp = &gamma_set_rx[gs*CI_MAX];
			res = &gamma_set_tx[ibr][gs*CI_MAX];
			offset = &gamma_offset[ibr][gs*CI_MAX];
			max = GENMASK(gamma_width[gs] - 1, 0);

			for (c = 0; c < CI_MAX; c++) {
				res[c] = mtp[c] + offset[c];
				res[c] = (res[c] < 0) ? 0 : res[c];
				res[c] = (res[c] > max) ? max : res[c];
			}
		}
	}

	gamma_reg_rx = &RX_BUF_67[167];
	for (ibr = 0; ibr <= EXTEND_BRIGHTNESS; ibr++) {
		memset(&S6E3FC5_SDC_GAMMA_NDARRAY_DATA[ibr][1], 0, GAMMA_LEGNTH);
		//memcpy(&S6E3FC5_SDC_GAMMA_NDARRAY_DATA[ibr][1], gamma_reg_rx, GAMMA_LEGNTH);

		gamma_reg_tx = &S6E3FC5_SDC_GAMMA_NDARRAY_DATA[ibr][1];	/* [0] is reg address */
		for (gs = 0; gs < GS_MAX; gs++) {
			res = &gamma_set_tx[ibr][gs*CI_MAX];

			for (c = 0; c < CI_MAX; c++) {
				put_unaligned_be16(res[c], gamma_reg_tx);
				gamma_reg_tx += DIV_ROUND_UP(gamma_width[gs], BITS_PER_BYTE);
			}
		}
	}
}

